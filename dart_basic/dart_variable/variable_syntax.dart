1) Rules for Naming Variables
Start with a Letter or _ (Underscore):

Variable names must start with a letter (a-z or A-Z)
or an underscore (_).
Names cannot start with a digit (0-9).

var name = 'John';  // Valid
var _age = 25;      // Valid
